import assert from "node:assert/strict";
import { readFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const frontendRoot = dirname(fileURLToPath(new URL("../package.json", import.meta.url)));

async function readProjectFile(relativePath) {
  return readFile(join(frontendRoot, relativePath), "utf8");
}

const packageJson = JSON.parse(await readProjectFile("package.json"));
assert.match(packageJson.scripts["generate:api"], /orval/);
assert.match(packageJson.scripts["check:api"], /check-api-generation\.mjs/);
assert.match(packageJson.scripts.check, /check:api/);
const orvalConfig = await readProjectFile("orval.config.mjs");
assert.match(orvalConfig, /client: "fetch"/);
assert.match(orvalConfig, /Generated by orval\./);

const openapiJson = JSON.parse(await readProjectFile("src/api/openapi.json"));
assert.equal(openapiJson.info.title, "Job Search Intelligence API");
assert.ok(openapiJson.paths["/health"].get);

const generatedClient = await readProjectFile("src/api/generated.ts");
assert.match(generatedClient, /Generated by orval\./);
assert.doesNotMatch(generatedClient, /\u{1F37A}/u);
assert.match(generatedClient, /export type healthHealthGetResponse200/);
assert.match(generatedClient, /export const healthHealthGet/);

const apiIndex = await readProjectFile("src/api/index.ts");
assert.match(apiIndex, /\.\/generated/);
